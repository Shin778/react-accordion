import './App.css';
import { useState } from 'react'


function App() {
  const [clicked, setclicked] = useState(false)


  const toggle = (i) => {
    if(clicked === i) {
      return setclicked(false)
    }

    setclicked(i)
  }

    return (
        <div className='wrapper'>
          <div className='accordion'>
            {data.map((item, i) => {
              return (
                <div className='item'>
                <div className={clicked === i? 'title active' :'title'} onClick={() => toggle(i)}>
                  {item.question}
                  <img className = {clicked === i? 'arrow active' :'arrow'} src="https://www.svgrepo.com/show/16711/right-arrow.svg"></img>
                </div>
                <div className = {clicked === i? 'content show' :'content'}>{item.answer}</div>
              </div>
              )
            })}
          </div>
        </div>
    )
}

